/**
 * 提取传入对象中的鱼和鱼珠信息
 * @param {*} obj
 * @returns
 */
export const HeroFillInfo = (obj) => {
  let temp = {};
  // 检查 obj 和 obj.heroes 是否存在
  if (obj && obj.heroes && typeof obj.heroes === "object") {
    Object.values(obj.heroes).forEach((hero) => {
      temp[hero.artifactId] = {
        FishInfo: FishMap[(hero.artifactId + "").substring(0, 4)],
        artifactId: hero.artifactId,
      };
      // 获取鱼珠技能信息，先检查 appendSkill 是否存在
      if (hero.appendSkill && Array.isArray(hero.appendSkill)) {
        hero.appendSkill.forEach((item) => {
          if (PearlMap[item.skillId]) {
            temp[hero.artifactId].PearlSkill = PearlMap[item.skillId];
          }
        });
      }
    });
  }
  // 检查 obj.pearlMap 是否存在
  if (obj && obj.pearlMap && typeof obj.pearlMap === "object") {
    Object.values(obj.pearlMap).forEach((item) => {
      if (temp[item.artifactId]) {
        // 检查 item.slotMap 是否存在
        if (item.slotMap && typeof item.slotMap === "object") {
          temp[item.artifactId].slotMap = Object.values(item.slotMap).map(
            (slot) => {
              return Object.assign({}, slot, color[slot.colorId]);
            },
          );
        }
      }
    });
  }
  return temp;
};

